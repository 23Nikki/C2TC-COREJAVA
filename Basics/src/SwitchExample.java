
public class SwitchExample {

	public static void main(String[] args) {
		int i=2;
		switch(i) {
		case 0:
			System.out.println("i is 0");
			break;
		case 1:
			System.out.println("i is 1");
			break;
		case 2:
			System.out.println("i is 2");
			break;
		default:
			System.out.println("full");
		}
	}

}
